public class CicloWhile {

	public static void main(String[] args) {

		// While
		int i = 0;
		while (i < 3) {
			System.out.println(i);
			i++;
		}

		System.out.println();

		// Do-While
		int c = 0;
		do {
			System.out.println(c);
			c++;
		} while (c < 3);

	}

}
